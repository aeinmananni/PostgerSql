-- درواقع این دو دستور نحوه عملکرده کوئری هارو نمایش میدن

-- EXPLAINE : 
-- اگر قصد داریم کوئری های خودمون رو مورد برسی قرار بدیم میتونیم از این روش این گزارش هارو تهیه کنیم



EXPLAIN ANALYZE SELECT
        username,contents
FROM
    instagram.users U
JOIN instagram.comments C
ON
    U.id = C.user_id WHERE U.username = 'USER4';

خب بنابر این این کوئری باعث میشود ما اطلاعاتی رو درباره نحوه اجرا شدن کوئری مورد نظر
و مدت زمانی که طول میکشد تا یک کوئری اجرا شود دریافت کنیم
هر کدام از اطلاعات خارج شده از نحوه گرفتن عملکرد کوئری نمایان گر یک گره خواهد بود

این عملیات ها گامی هستند برای دسترسی به برخی از داده هایی که در دالخ پایگاه داده ذخیره میشوند ALTER
و ما میتونیم به اون ها دتسرسی پیدا کنیم

